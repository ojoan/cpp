#include <stdio.h>

int main() {

    printf("10 < 15 --> %d\n", 10 < 15);
    printf("10 > 15 --> %d\n", 10 > 15);
    printf("10 <= 15 --> %d\n", 10 <= 15);
    printf("10 >= 15 --> %d\n", 10 >= 15);
    printf("10 == 15 --> %d\n", 10 == 15);
    printf("10 == 10 --> %d\n", 10 == 10);
    printf("10 != 15 --> %d\n", 10 != 15);
    printf("10 != 10 --> %d\n", 10 != 10);



    return 0;
}